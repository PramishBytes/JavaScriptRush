// JavaScript UnderScore (_)

// Since JavaScript treats underscore as a letter, identifiers containing _are valid variable names:

let _lastName = "Pramish";
let _x=2; 
let _100=5;
console.log(_lastName,'\n'+_x,'\n'+_100);

/*
Pramish 
2 
5
*/

